<br>"Looks great. Let me know when it launches."
<br>- Bad meeting. A compliment plus a stalling tactic is the classic way to end a pitch for something you’re never going to buy. A polite way to say, “Don’t call me, I’ll call you.” 
<br>
<br>"There are a couple people I can introduce you to when you're ready."
<br>- Nearly a good meeting, but right now it’s bad. There’s some data here, but probably not as much as you initially hope.
<br>- To fix it, try to convert fuzzy promises into something more concrete. For example: who does he want to introduce you to and what does “ready” mean? And why can’t he make the intro now? This isn’t about being pushy. But you do need to distinguish between legitimate offers and polite gesturing. Knowing what “ready” means can also give you a better sense of what your short-term goals should be.
<br>
<br>"What are the next steps?"
<br>- Good meeting. A classic good meeting conclusion. 
<br>- It's worth noting that you need to know your next steps to benefit from this. If you have to say, “Let me have a think about that and get back to you,” then you’ve ruined a perfectly good meeting.
<br>- Of course, even clear next steps can be a lie. Everything can be a lie. But with next steps in your pocket, you've got a fighting chance.
<br>
<br>"I would definitely buy that."
<br>- Bad meeting. Danger.
<br>- To fix it, you need to shift from fuzzy future promises into concrete current commitments. For example, you could ask for a letter of intent, a pre-purchase or deposit, or intros to other decision makers and team members. Kickstarter is so wonderful because it forces customers who say they would buy it to actually pull out a credit card and commit.
<br>
<br>"When can we start the trial?"
<br>- What are they giving up to try it out? If your trial is too "cheap" to try, you can try to increase the cost of it. For example, you could ask to write a case study about them after they've spent 2 weeks using it. Or you could get them to promise to try using it with their team for at least a week. If you've got a more developed product, you can take their credit card and just charge them nothing if they cancel within 30 days. The more they are giving up for you, the more seriously you can take their validation.
<br>
<br>"Can I buy the prototype?"
<br>- Great meeting. The best meeting conclusion I've seen is the rarely-heard but always appreciated, “Can I buy the prototype?” 
