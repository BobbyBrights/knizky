<br>Byt trpezlivy znamena trpiet. Podla plodov prace sa luia poznaju navzajom, avsak podla utrpenia zistuju, kym naozaj su. Utrpenie neznamena len strpenie telesnych a dusevnych muk, je to aj radost. Zajac chyteny v pasci zaziva telesne muky, ale nijako viac netrpi. Ukrizovany zaziva telesne a dusevne muky, ale aj intenzivnu radost a stastie. Proliemelyselny delnik je vacsinou ako zajac chyteny do pasce, umelec casto byva ten, koho pribili na kriz. 
