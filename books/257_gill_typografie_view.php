<br>Rozmery okrajov. Take, aby oddelili text od okolia. Vonkajsi a spodny vacsi, lebo na nich sa kniha drzi. Aby text nevyzeral, ze ho moc siroke okraje tlacia dostredu knihy, pouzivame nasledujuce pravidlo: uzky vnutorny, mierne sirsi horny, Vonkajsiaspon 2x ako vnutorny a spodny mierne sirsi ako ostatne.
<br>Priklad: strana 5x7.5 palca, vnutorny 0.5, horny 0.75,  vonkajsi 1, spodny 1 + 1/6. To je 34 riadkov 12 bodoveho pisma s priemernou dlzkou 10 az 12 slov. 
<br>Uvedene rozmery lze oznacit za normu.
